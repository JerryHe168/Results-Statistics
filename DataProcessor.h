#pragma once
#include "DataTypes.h"
#include <vector>
#include <string>

// ============================================================
// DataProcessor 类 - 数据处理器
// ============================================================
// 核心业务逻辑处理类
// 
// 主要功能：
// 1. 数据匹配 - 将成绩清单与报名信息关联
// 2. 结果导出 - 导出结果到Excel或CSV文件
// 
// 核心算法：
// - 建立两个独立的映射表（男生组号->姓名，女生组号->姓名）
// - 根据成绩清单的组号分别查找男生和女生姓名
// - 拼接后输出结果
// ============================================================
class DataProcessor {
public:
    // --------------------------------------------------------
    // 构造函数
    // --------------------------------------------------------
    DataProcessor();

    // --------------------------------------------------------
    // 析构函数
    // --------------------------------------------------------
    ~DataProcessor();

    // --------------------------------------------------------
    // 处理数据匹配
    // --------------------------------------------------------
    // 参数：
    //   participants - 报名信息列表
    //   scoreEntries - 成绩条目列表
    //   results - 输出参数，存储匹配后的结果列表
    // 
    // 返回值：
    //   true  - 处理成功
    //   false - 处理失败
    // 
    // 工作原理：
    //   1. 遍历所有报名信息，建立两个映射表：
    //      - maleMap:   男生组号 -> 男生姓名
    //      - femaleMap: 女生组号 -> 女生姓名
    // 
    //   2. 遍历所有成绩条目：
    //      - 从成绩条目中提取组号
    //      - 在maleMap中查找该组号对应的男生姓名
    //      - 在femaleMap中查找该组号对应的女生姓名
    //      - 拼接姓名（男生姓名 + 空格 + 女生姓名）
    //      - 生成ResultEntry并添加到结果列表
    // 
    // 设计原因：
    //   原来的逻辑假设每一行的男生和女生是同一组的，但实际上：
    //   - 男生编号"23A"和女生编号"23B"可能在报名信息的不同行
    //   - 需要分别建立映射表，独立匹配
    // 
    // 示例：
    //   报名信息：
    //     行1: 男生23A(罗晓东) + 女生16B(严玉蓝)
    //     行2: 男生21A(冼俊炫) + 女生23B(梁馨尹)
    //   
    //   建立的映射表：
    //     maleMap[23] = "罗晓东"
    //     maleMap[21] = "冼俊炫"
    //     femaleMap[16] = "严玉蓝"
    //     femaleMap[23] = "梁馨尹"
    //   
    //   成绩条目 23组：
    //     查找 maleMap[23] -> "罗晓东"
    //     查找 femaleMap[23] -> "梁馨尹"
    //     拼接结果: "罗晓东 梁馨尹"
    // --------------------------------------------------------
    bool ProcessData(const std::vector<Participant>& participants,
                     const std::vector<ScoreEntry>& scoreEntries,
                     std::vector<ResultEntry>& results);

    // --------------------------------------------------------
    // 导出结果到Excel文件
    // --------------------------------------------------------
    // 参数：
    //   filePath - 输出Excel文件路径
    //   results - 结果条目列表
    // 
    // 返回值：
    //   true  - 导出成功
    //   false - 导出失败
    // 
    // 实现：
    //   使用COM自动化创建Excel工作簿
    //   写入表头：Rank, Group, Names, Score
    //   逐行写入结果数据
    //   根据文件扩展名选择格式：
    //     .xls  -> Excel 97-2003 格式 (FileFormat=56)
    //     .xlsx -> Excel 2007+ 格式 (FileFormat=51)
    // --------------------------------------------------------
    bool ExportResults(const std::wstring& filePath, const std::vector<ResultEntry>& results);

    // --------------------------------------------------------
    // 导出结果到CSV文件
    // --------------------------------------------------------
    // 参数：
    //   filePath - 输出CSV文件路径
    //   results - 结果条目列表
    // 
    // 返回值：
    //   true  - 导出成功
    //   false - 导出失败
    // 
    // 实现：
    //   写入UTF-8 BOM头（确保Excel正确识别中文字符）
    //   写入表头：Rank,Group,Names,Score
    //   逐行写入结果数据
    //   使用EscapeCsvField处理可能包含特殊字符的字段
    // 
    // CSV格式：
    //   Rank,Group,Names,Score
    //   1,13组,徐炳南 聂洁,0:37:06
    //   2,22组,李忠 邱思佳,0:38:49
    // --------------------------------------------------------
    bool ExportResultsToCsv(const std::wstring& filePath, const std::vector<ResultEntry>& results);

private:
    // --------------------------------------------------------
    // 将宽字符字符串转换为UTF-8字符串
    // --------------------------------------------------------
    // 参数：
    //   wstr - 宽字符字符串
    // 
    // 返回值：
    //   UTF-8编码的字符串
    // 
    // 实现：
    //   使用Windows API函数 WideCharToMultiByte
    //   代码页：CP_UTF8
    // --------------------------------------------------------
    std::string WStringToString(const std::wstring& wstr);

    // --------------------------------------------------------
    // 转义CSV字段
    // --------------------------------------------------------
    // 参数：
    //   field - 原始字段值
    // 
    // 返回值：
    //   转义后的字段值
    // 
    // 工作原理：
    //   1. 检查字段是否包含特殊字符（逗号、双引号、换行符、回车符）
    //   2. 如果不包含特殊字符，直接返回原字符串
    //   3. 如果包含特殊字符：
    //      - 用双引号包裹整个字段
    //      - 将字段内的双引号替换为两个双引号（转义）
    // 
    // 示例：
    //   "张三,李四"     -> "\"张三,李四\""
    //   "说"你好"世界"  -> "\"说""你好""世界\""
    //   "正常文本"     -> "正常文本"
    // --------------------------------------------------------
    std::string EscapeCsvField(const std::wstring& field);
};
