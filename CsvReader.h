#pragma once
#include "DataTypes.h"
#include <vector>
#include <string>

// ============================================================
// CsvReader 类 - CSV文件读取器
// ============================================================
// 负责从CSV文件（逗号分隔值格式）中读取数据
// 与ExcelReader具有相同的接口，实现多态读取
// 
// 主要功能：
// 1. 读取报名信息CSV
// 2. 读取成绩清单CSV
// 
// 支持的CSV特性：
// - UTF-8编码（带BOM或不带BOM）
// - 逗号分隔符
// - 双引号包裹的字段（支持字段内包含逗号、引号、换行符）
// - 自动跳过表头行
// - Windows/Unix换行符兼容
// ============================================================
class CsvReader {
public:
    // --------------------------------------------------------
    // 构造函数
    // --------------------------------------------------------
    CsvReader();

    // --------------------------------------------------------
    // 析构函数
    // --------------------------------------------------------
    ~CsvReader();

    // --------------------------------------------------------
    // 读取报名信息CSV
    // --------------------------------------------------------
    // 参数：
    //   filePath - CSV文件路径
    //   participants - 输出参数，存储读取到的报名信息列表
    // 
    // 返回值：
    //   true  - 读取成功
    //   false - 读取失败
    // 
    // CSV文件格式要求：
    //   第1列：男生编号（如 "12A", "18A"）
    //   第2列：男生姓名
    //   第3列：女生编号（如 "17B", "13B"）
    //   第4列：女生姓名
    //   第1行：表头（可选，会自动跳过）
    // 
    // 示例：
    //   男生编号,男生姓名,女生编号,女生姓名
    //   12A,陈盛,17B,张飞娴
    //   18A,孔令潇,13B,聂洁
    // --------------------------------------------------------
    bool ReadRegistrationInfo(const std::wstring& filePath, std::vector<Participant>& participants);

    // --------------------------------------------------------
    // 读取成绩清单CSV
    // --------------------------------------------------------
    // 参数：
    //   filePath - CSV文件路径
    //   scoreEntries - 输出参数，存储读取到的成绩条目列表
    // 
    // 返回值：
    //   true  - 读取成功
    //   false - 读取失败
    // 
    // CSV文件格式要求：
    //   第1列：名次（如 1, 2, 3）
    //   第2列：组别（如 "13组", "22组"）
    //   第3列：成绩时间（如 "0:37:06"）
    //   第1行：表头（可选，会自动跳过）
    // 
    // 示例：
    //   名次,组别,成绩
    //   1,13组,0:37:06
    //   2,22组,0:38:49
    // --------------------------------------------------------
    bool ReadScoreList(const std::wstring& filePath, std::vector<ScoreEntry>& scoreEntries);

private:
    // --------------------------------------------------------
    // 从编号中提取组号
    // --------------------------------------------------------
    // 与ExcelReader::ExtractGroupNumber功能相同
    // 使用正则表达式匹配字符串中的第一个连续数字序列
    // --------------------------------------------------------
    int ExtractGroupNumber(const std::wstring& id);

    // --------------------------------------------------------
    // 解析CSV行
    // --------------------------------------------------------
    // 参数：
    //   line - CSV文件中的一行文本
    // 
    // 返回值：
    //   分割后的字段列表
    // 
    // 工作原理：
    //   1. 遍历每一个字符
    //   2. 遇到双引号时切换"引号内状态"
    //   3. 在引号内，逗号不做为分隔符
    //   4. 在引号内，连续两个双引号表示一个双引号字符
    //   5. 在引号外，逗号做为字段分隔符
    // 
    // 示例：
    //   "张三,李四",王五 -> ["张三,李四", "王五"]
    //   "说""你好"",世界" -> ["说\"你好\",世界"]
    // --------------------------------------------------------
    std::vector<std::wstring> SplitCsvLine(const std::wstring& line);

    // --------------------------------------------------------
    // 去除字符串两端的空白和引号
    // --------------------------------------------------------
    // 参数：
    //   str - 待处理的字符串
    // 
    // 返回值：
    //   处理后的字符串
    // 
    // 去除的字符：
    //   - 空格 ' '
    //   - 制表符 '\t'
    //   - 双引号 '"'
    // --------------------------------------------------------
    std::wstring Trim(const std::wstring& str);

    // --------------------------------------------------------
    // 将UTF-8字符串转换为宽字符字符串
    // --------------------------------------------------------
    // 参数：
    //   str - UTF-8编码的字符串
    // 
    // 返回值：
    //   宽字符字符串（wstring）
    // 
    // 实现：
    //   使用Windows API函数 MultiByteToWideChar
    //   代码页：CP_UTF8
    // --------------------------------------------------------
    std::wstring StringToWString(const std::string& str);
};
