#pragma execution_character_set("utf-8")

// ============================================================
// CsvReader.cpp - CSV文件读取器实现
// ============================================================
// 本文件实现了读取CSV格式文件的功能
// 
// 主要功能：
// 1. StringToWString：UTF-8字符串转换为宽字符字符串
// 2. Trim：去除字符串首尾的空白和引号
// 3. SplitCsvLine：解析CSV格式的一行数据
// 4. ExtractGroupNumber：从编号中提取组号
// 5. ReadRegistrationInfo：读取报名信息CSV文件
// 6. ReadScoreList：读取成绩清单CSV文件
// 
// CSV格式支持：
// - UTF-8编码（带BOM或不带BOM）
// - 逗号分隔符
// - 双引号包裹的字段（支持字段内包含逗号、引号、换行符）
// - 引号转义（两个双引号表示一个双引号）
// - 自动跳过表头行
// - 支持中文路径
// 
// 为什么使用 _wfopen_s 而不是 std::ifstream？
// - std::ifstream 不支持UTF-8编码的中文路径
// - _wfopen_s 接受宽字符路径，可以正确处理中文字符
// ============================================================

