// ============================================================
// UTF-8字符串转换为宽字符字符串
// ============================================================
// 参数：
//   str - UTF-8编码的字符串
// 
// 返回值：
//   宽字符字符串（UTF-16，Windows原生格式）
// 
// 工作原理：
//   使用Windows API函数 MultiByteToWideChar 进行编码转换
// 
// 执行流程：
//   1. 检查输入是否为空
//   2. 第一次调用 MultiByteToWideChar，传入NULL缓冲区，获取所需大小
//   3. 根据返回大小分配缓冲区
//   4. 第二次调用 MultiByteToWideChar，执行实际转换
//   5. 返回转换后的字符串
// 
// 为什么要调用两次？
// - 第一次调用获取所需的缓冲区大小
// - 这样可以精确分配内存，避免缓冲区溢出
// - 这是Windows API的常见模式
// ============================================================
std::wstring CsvReader::StringToWString(const std::string& str) {
    // 空字符串直接返回
    if (str.empty()) {
        return L"";
    }

    // 第一次调用：获取所需的缓冲区大小
    // 参数：
    //   CP_UTF8 - 源编码为UTF-8
    //   0 - 默认转换标志
    //   str.c_str() - 源字符串
    //   -1 - 源字符串长度（自动计算，包含null终止符）
    //   NULL - 目标缓冲区（NULL表示只计算大小）
    //   0 - 目标缓冲区大小
    int size = MultiByteToWideChar(CP_UTF8, 0, str.c_str(), -1, NULL, 0);
    
    // 检查是否失败
    if (size <= 0) {
        return L"";
    }

    // 分配缓冲区
    // size - 1 是因为 MultiByteToWideChar 返回的大小包含null终止符
    // 而 std::wstring 不需要额外的null终止符空间
    std::wstring result(size - 1, 0);

    // 第二次调用：执行实际转换
    MultiByteToWideChar(CP_UTF8, 0, str.c_str(), -1, &result[0], size);

    return result;
}

// ============================================================
// 去除字符串首尾的空白和引号
// ============================================================
// 参数：
//   str - 原始字符串
// 
// 返回值：
//   处理后的字符串
// 
// 工作原理：
//   1. 查找第一个非空白非引号字符的位置
//   2. 查找最后一个非空白非引号字符的位置
//   3. 提取中间的子字符串
// 
// 要去除的字符：
//   - 空格（' '）
//   - 制表符（'\t'）
//   - 双引号（'"'）
// 
// 为什么需要这个函数？
// - CSV文件中的字段可能被空格包围
// - 字段可能被双引号包裹
// - 去除这些字符后才能得到实际的数据
// 
// 示例：
//   "  张三  "  ->  "张三"
//   "\"李四\""  ->  "李四"
//   ""          ->  ""（空字符串）
// ============================================================
std::wstring CsvReader::Trim(const std::wstring& str) {
    // 查找第一个非空白非引号字符的位置
    // find_first_not_of 返回第一个不在参数列表中的字符的索引
    // 如果所有字符都在列表中，返回 std::wstring::npos
    size_t start = str.find_first_not_of(L" \t\"");
    
    // 如果所有字符都是空白或引号，返回空字符串
    if (start == std::wstring::npos) {
        return L"";
    }

    // 查找最后一个非空白非引号字符的位置
    // find_last_not_of 返回最后一个不在参数列表中的字符的索引
    size_t end = str.find_last_not_of(L" \t\"");

    // 提取子字符串
    // substr 参数：起始位置，长度
    // 长度 = 结束位置 - 起始位置 + 1
    return str.substr(start, end - start + 1);
}

// ============================================================
// 解析CSV格式的一行数据
// ============================================================
// 参数：
//   line - CSV格式的一行字符串
// 
// 返回值：
//   解析后的字段列表（std::vector<std::wstring>）
// 
// CSV格式规则（RFC 4180）：
//   1. 字段用逗号分隔
//   2. 如果字段包含逗号、双引号或换行符，必须用双引号包裹
//   3. 字段中的双引号必须用两个双引号表示（转义）
// 
// 解析算法：
//   1. 初始化：当前字段为空，不在引号中
//   2. 遍历每个字符：
//      a. 如果是双引号：
//         - 如果在引号中且下一个字符也是双引号：输出一个双引号，跳过下一个字符
//         - 否则：切换引号状态
//      b. 如果是逗号且不在引号中：
//         - 结束当前字段，Trim后添加到结果列表
//         - 重置当前字段
//      c. 其他字符：
//         - 添加到当前字段
//   3. 处理最后一个字段
// 
// 示例：
//   "张三,李四",王五,"赵""钱"
//   解析后：["张三,李四", "王五", "赵\"钱"]
// 
//   1,23组,0:37:06
//   解析后：["1", "23组", "0:37:06"]
// ============================================================
std::vector<std::wstring> CsvReader::SplitCsvLine(const std::wstring& line) {
    std::vector<std::wstring> result;  // 存储解析后的字段
    std::wstring current;               // 当前正在构建的字段
    bool inQuotes = false;              // 是否在引号中

    // 遍历每个字符
    for (size_t i = 0; i < line.length(); i++) {
        wchar_t c = line[i];

        if (c == L'"') {
            // ------------------------------------------------
            // 遇到双引号
            // ------------------------------------------------
            // 情况1：在引号中，且下一个字符也是双引号
            //       这是转义序列，表示一个双引号
            // 
            // 情况2：在引号中，下一个字符不是双引号
            //       这是结束引号
            // 
            // 情况3：不在引号中
            //       这是开始引号
            // ------------------------------------------------
            if (inQuotes && i + 1 < line.length() && line[i + 1] == L'"') {
                // 情况1：转义的双引号（两个双引号表示一个）
                current += L'"';   // 输出一个双引号
                i++;                // 跳过下一个双引号
            }
            else {
                // 情况2或3：切换引号状态
                // inQuotes = true  -> false（结束引号）
                // inQuotes = false -> true（开始引号）
                inQuotes = !inQuotes;
            }
        }
        else if (c == L',' && !inQuotes) {
            // ------------------------------------------------
            // 遇到逗号且不在引号中
            // ------------------------------------------------
            // 这是字段分隔符
            // 结束当前字段，添加到结果列表
            // ------------------------------------------------
            
            // Trim当前字段（去除首尾空白和引号）
            result.push_back(Trim(current));
            
            // 重置当前字段，准备下一个字段
            current.clear();
        }
        else {
            // ------------------------------------------------
            // 普通字符
            // ------------------------------------------------
            // 添加到当前字段
            // ------------------------------------------------
            current += c;
        }
    }

    // --------------------------------------------------------
    // 处理最后一个字段
    // --------------------------------------------------------
    // 循环结束后，current中还有最后一个字段
    // 需要Trim后添加到结果列表
    // --------------------------------------------------------
    result.push_back(Trim(current));

    return result;
}

// ============================================================
// 从编号中提取组号
// ============================================================
// 参数：
//   id - 编号字符串，如 "12A", "18B", "13组", "第5组"
// 
// 返回值：
//   提取的组号（整数），如果无法提取数字则返回 -1
// 
// 工作原理：
//   使用正则表达式匹配字符串中的第一个连续数字序列
// 
// 支持的格式：
//   - "12A"   -> 12  （男生编号格式）
//   - "18B"   -> 18  （女生编号格式）
//   - "13组"  -> 13  （组别格式）
//   - "第5组" -> 5   （中文组别格式）
// 
// 正则表达式说明：
//   L"(\\d+)"  - 匹配一个或多个连续数字
//   \\d        - 匹配任意数字（等价于[0-9]）
//   +          - 匹配一个或多个
//   ()         - 捕获组，用于提取匹配的内容
// 
// 注意：
//   - 只提取第一个数字序列
//   - 例如 "12A34" 只提取 "12"，返回 12
//   - 如果没有数字，返回 -1
// ============================================================
int CsvReader::ExtractGroupNumber(const std::wstring& id) {
    // 创建正则表达式：匹配一个或多个连续数字
    // L"(\\d+)" 中的 L 表示宽字符字符串
    // (\\d+) 中的 \\ 是转义的 \，在正则表达式中表示 \d
    std::wregex regex(L"(\\d+)");
    std::wsmatch match;  // 存储匹配结果

    // 在id中搜索匹配正则表达式的内容
    // regex_search 在字符串中搜索匹配项
    // 与 regex_match 不同，regex_match 要求整个字符串匹配
    if (std::regex_search(id, match, regex)) {
        // match[1] 是第一个捕获组的内容
        // match[0] 是整个匹配的内容
        // match[1], match[2]... 是各个捕获组的内容
        // 
        // 例如：id = "12A"
        // match[0] = "12"（整个匹配）
        // match[1] = "12"（第一个捕获组）
        // 
        // .str() 将匹配结果转换为 wstring
        // std::stoi 将 wstring 转换为整数
        return std::stoi(match[1].str());
    }

    // 如果没有找到数字，返回 -1 表示失败
    return -1;
}

// ============================================================
// 读取报名信息CSV文件
// ============================================================
// 参数：
//   filePath - CSV文件路径
//   participants - 输出参数，存储读取到的报名信息列表
// 
// 返回值：
//   true  - 读取成功
//   false - 读取失败
// 
// CSV文件格式要求：
//   第1列：男生编号（如 "12A", "18A"）
//   第2列：男生姓名
//   第3列：女生编号（如 "17B", "13B"）
//   第4列：女生姓名
//   第1行：表头（可选，会自动检测并跳过）
// 
// 自动跳过表头的规则：
//   检查第1行的第1列是否包含以下关键字：
//   - "男生" 或 "male"（男生编号列标题）
//   - "编号"（通用编号列标题）
//   如果包含，则认为是表头行，跳过
// 
// 执行流程：
//   1. 使用 _wfopen_s 以二进制模式打开文件（支持中文路径）
//   2. 读取整个文件内容到内存
//   3. 检查并去除UTF-8 BOM（如果存在）
//   4. 将UTF-8内容转换为宽字符字符串
//   5. 逐行解析：
//      a. 去除行尾的回车符（\r）
//      b. 跳过空行
//      c. 如果是第一行，检查是否为表头
//      d. 解析CSV行，提取字段
//      e. 填充Participant结构体
//      f. 提取组号
//      g. 添加到结果列表（过滤空行）
//   6. 关闭文件
// 
// 注意事项：
//   - 每行必须至少有4个字段
//   - 男生姓名或女生姓名至少有一个不为空才会添加到结果
//   - 使用宽字符路径支持中文文件名
// ============================================================
bool CsvReader::ReadRegistrationInfo(const std::wstring& filePath, std::vector<Participant>& participants) {
    // 清空结果列表，准备接收新数据
    participants.clear();

    // --------------------------------------------------------
    // 使用 _wfopen_s 打开文件（支持中文路径）
    // --------------------------------------------------------
    // 参数：
    //   &file      - 输出参数，接收文件指针
    //   filePath.c_str() - 宽字符路径
    //   L"rb"      - 打开模式：r=读取，b=二进制模式
    // 
    // 为什么用二进制模式？
    // - 二进制模式不会自动转换换行符
    // - 我们需要读取原始的文件内容
    // - 然后自己处理换行符和编码
    // --------------------------------------------------------
    FILE* file = NULL;
    errno_t err = _wfopen_s(&file, filePath.c_str(), L"rb");
    if (err != 0 || file == NULL) {
        std::wcerr << L"Failed to open CSV file: " << filePath << std::endl;
        return false;
    }

    // --------------------------------------------------------
    // 读取整个文件内容到内存
    // --------------------------------------------------------
    // 步骤：
    // 1. fseek 到文件末尾
    // 2. ftell 获取文件大小
    // 3. fseek 回到文件开头
    // 4. fread 读取整个文件
    // 
    // 为什么要读取整个文件？
    // - CSV文件通常不大
    // - 一次性读取比逐行读取更高效
    // - 便于处理UTF-8编码和BOM
    // --------------------------------------------------------
    fseek(file, 0, SEEK_END);      // 定位到文件末尾
    long fileSize = ftell(file);   // 获取文件大小（字节数）
    fseek(file, 0, SEEK_SET);      // 定位回文件开头

    // 分配缓冲区
    std::string content(fileSize, 0);

    // 读取整个文件内容
    fread(&content[0], 1, fileSize, file);

    // 关闭文件（读取完成后立即关闭）
    fclose(file);

    // --------------------------------------------------------
    // 检查并去除UTF-8 BOM
    // --------------------------------------------------------
    // UTF-8 BOM 是三个字节：0xEF 0xBB 0xBF
    // 有些编辑器（如Windows记事本）会在保存UTF-8文件时添加BOM
    // 
    // 为什么要去除BOM？
    // - BOM不是实际内容的一部分
    // - 如果不去除，会导致第一行的第一个字符异常
    // - 例如："男生编号" 变成 "\xEF\xBB\xBF男生编号"
    // 
    // 如何检测BOM？
    // 检查文件前3个字节是否为 0xEF 0xBB 0xBF
    // --------------------------------------------------------
    if (content.length() >= 3 && 
        (unsigned char)content[0] == 0xEF && 
        (unsigned char)content[1] == 0xBB && 
        (unsigned char)content[2] == 0xBF) {
        // 检测到BOM，去除前3个字节
        content = content.substr(3);
    }

    // --------------------------------------------------------
    // 将UTF-8内容转换为宽字符字符串
    // --------------------------------------------------------
    // Windows原生使用UTF-16（宽字符）
    // 我们需要将UTF-8转换为UTF-16才能正确处理中文字符
    // --------------------------------------------------------
    std::wstring wcontent = StringToWString(content);

    // 使用 wstringstream 逐行读取
    std::wistringstream iss(wcontent);
    std::wstring line;

    // --------------------------------------------------------
    // 逐行解析
    // --------------------------------------------------------
    // isFirstLine 标记：
    // - true：正在处理第一行（可能是表头）
    // - false：正在处理数据行
    // 
    // 表头检测逻辑：
    // 1. 解析第一行
    // 2. 检查是否至少有2个字段
    // 3. 将第一个字段转换为小写
    // 4. 检查是否包含 "男生"、"male" 或 "编号"
    // 5. 如果包含，认为是表头，跳过这一行
    // 6. 如果不包含，认为是数据行，继续处理
    // --------------------------------------------------------
    bool isFirstLine = true;
    while (std::getline(iss, line)) {
        // ----------------------------------------------------
        // 去除行尾的回车符（\r）
        // ----------------------------------------------------
        // Windows换行符是 \r\n
        // Unix换行符是 \n
        // std::getline 默认以 \n 作为分隔符
        // 如果文件是Windows格式，读取的行会包含末尾的 \r
        // 需要手动去除
        // ----------------------------------------------------
        if (!line.empty() && line.back() == L'\r') {
            line.pop_back();
        }

        // 跳过空行
        if (line.empty()) {
            continue;
        }

        // ----------------------------------------------------
        // 处理第一行（可能是表头）
        // ----------------------------------------------------
        if (isFirstLine) {
            // 解析第一行
            std::vector<std::wstring> header = SplitCsvLine(line);

            // 检查是否有足够的字段
            if (header.size() >= 2) {
                // 将第一个字段转换为小写，进行不区分大小写的比较
                std::wstring lowerHeader = header[0];
                std::transform(lowerHeader.begin(), lowerHeader.end(), lowerHeader.begin(), ::towlower);

                // 检查是否包含表头关键字
                // "男生" = 男生编号列
                // "male" = 英文的男生编号列
                // "编号" = 通用的编号列
                if (lowerHeader.find(L"男生") != std::wstring::npos || 
                    lowerHeader.find(L"male") != std::wstring::npos ||
                    lowerHeader.find(L"编号") != std::wstring::npos) {
                    // 检测到表头，跳过这一行
                    isFirstLine = false;
                    continue;
                }
            }

            // 不是表头，标记第一行已处理
            // 这一行会被当作数据行处理
            isFirstLine = false;
        }

        // ----------------------------------------------------
        // 解析CSV行
        // ----------------------------------------------------
        std::vector<std::wstring> columns = SplitCsvLine(line);

        // 检查是否有足够的字段
        // 报名信息需要至少4列：男生编号、男生姓名、女生编号、女生姓名
        if (columns.size() < 4) {
            continue;  // 字段不足，跳过这一行
        }

        // ----------------------------------------------------
        // 填充Participant结构体
        // ----------------------------------------------------
        // 列映射：
        // columns[0] = 男生编号
        // columns[1] = 男生姓名
        // columns[2] = 女生编号
        // columns[3] = 女生姓名
        // ----------------------------------------------------
        Participant participant;

        // 男生信息
        participant.maleId = columns[0];           // 男生编号
        participant.maleName = columns[1];         // 男生姓名

        // 女生信息
        participant.femaleId = columns[2];         // 女生编号
        participant.femaleName = columns[3];       // 女生姓名

        // 提取组号
        // 男生编号和女生编号分别提取组号
        // 支持男生和女生在不同组的情况
        participant.maleGroupNumber = ExtractGroupNumber(participant.maleId);
        participant.femaleGroupNumber = ExtractGroupNumber(participant.femaleId);

        // ----------------------------------------------------
        // 添加到结果列表
        // ----------------------------------------------------
        // 过滤条件：男生姓名或女生姓名至少有一个不为空
        // 这是为了跳过空行或无效行
        // ----------------------------------------------------
        if (!participant.maleName.empty() || !participant.femaleName.empty()) {
            participants.push_back(participant);
        }
    }

    return true;
}

// ============================================================
// 读取成绩清单CSV文件
// ============================================================
// 参数：
//   filePath - CSV文件路径
//   scoreEntries - 输出参数，存储读取到的成绩条目列表
// 
// 返回值：
//   true  - 读取成功
//   false - 读取失败
// 
// CSV文件格式要求：
//   第1列：名次（如 1, 2, 3）
//   第2列：组别（如 "13组", "22组"）
//   第3列：成绩时间（如 "0:37:06"）
//   第1行：表头（可选，会自动检测并跳过）
// 
// 自动跳过表头的规则：
//   检查第1行的第1列是否包含以下关键字：
//   - "名次"、"排名"（中文排名列标题）
//   - "rank"（英文排名列标题）
//   如果包含，则认为是表头行，跳过
// 
// 执行流程：
//   1. 使用 _wfopen_s 以二进制模式打开文件（支持中文路径）
//   2. 读取整个文件内容到内存
//   3. 检查并去除UTF-8 BOM（如果存在）
//   4. 将UTF-8内容转换为宽字符字符串
//   5. 逐行解析：
//      a. 去除行尾的回车符（\r）
//      b. 跳过空行
//      c. 如果是第一行，检查是否为表头
//      d. 解析CSV行，提取字段
//      e. 将名次转换为整数（使用try-catch处理异常）
//      f. 填充ScoreEntry结构体
//      g. 提取组号
//      h. 添加到结果列表（过滤名次 <= 0 的行）
//   6. 关闭文件
// 
// 与 ReadRegistrationInfo 的主要区别：
// 1. 列映射不同：
//    - 第1列 -> 名次（rank，需要转换为整数）
//    - 第2列 -> 组别（group）
//    - 第3列 -> 成绩时间（time）
// 
// 2. 表头检测关键字不同：
//    - "名次"、"排名"、"rank"
// 
// 3. 过滤条件不同：
//    - 名次 > 0 才添加到结果列表
// 
// 4. 数据类型处理不同：
//    - 名次需要从字符串转换为整数
//    - 使用 try-catch 捕获转换异常
// ============================================================
bool CsvReader::ReadScoreList(const std::wstring& filePath, std::vector<ScoreEntry>& scoreEntries) {
    // 清空结果列表，准备接收新数据
    scoreEntries.clear();

    // --------------------------------------------------------
    // 使用 _wfopen_s 打开文件（支持中文路径）
    // --------------------------------------------------------
    FILE* file = NULL;
    errno_t err = _wfopen_s(&file, filePath.c_str(), L"rb");
    if (err != 0 || file == NULL) {
        std::wcerr << L"Failed to open CSV file: " << filePath << std::endl;
        return false;
    }

    // --------------------------------------------------------
    // 读取整个文件内容到内存
    // --------------------------------------------------------
    fseek(file, 0, SEEK_END);      // 定位到文件末尾
    long fileSize = ftell(file);   // 获取文件大小（字节数）
    fseek(file, 0, SEEK_SET);      // 定位回文件开头

    // 分配缓冲区
    std::string content(fileSize, 0);

    // 读取整个文件内容
    fread(&content[0], 1, fileSize, file);

    // 关闭文件
    fclose(file);

    // --------------------------------------------------------
    // 检查并去除UTF-8 BOM
    // --------------------------------------------------------
    if (content.length() >= 3 && 
        (unsigned char)content[0] == 0xEF && 
        (unsigned char)content[1] == 0xBB && 
        (unsigned char)content[2] == 0xBF) {
        // 检测到BOM，去除前3个字节
        content = content.substr(3);
    }

    // --------------------------------------------------------
    // 将UTF-8内容转换为宽字符字符串
    // --------------------------------------------------------
    std::wstring wcontent = StringToWString(content);
    std::wistringstream iss(wcontent);
    std::wstring line;

    // --------------------------------------------------------
    // 逐行解析
    // --------------------------------------------------------
    bool isFirstLine = true;
    while (std::getline(iss, line)) {
        // 去除行尾的回车符
        if (!line.empty() && line.back() == L'\r') {
            line.pop_back();
        }

        // 跳过空行
        if (line.empty()) {
            continue;
        }

        // ----------------------------------------------------
        // 处理第一行（可能是表头）
        // ----------------------------------------------------
        // 表头检测关键字：
        // "名次"、"排名" = 中文排名列标题
        // "rank" = 英文排名列标题
        // ----------------------------------------------------
        if (isFirstLine) {
            // 解析第一行
            std::vector<std::wstring> header = SplitCsvLine(line);

            // 检查是否有足够的字段
            if (header.size() >= 2) {
                // 将第一个字段转换为小写
                std::wstring lowerHeader = header[0];
                std::transform(lowerHeader.begin(), lowerHeader.end(), lowerHeader.begin(), ::towlower);

                // 检查是否包含排名相关关键字
                if (lowerHeader.find(L"名次") != std::wstring::npos || 
                    lowerHeader.find(L"rank") != std::wstring::npos ||
                    lowerHeader.find(L"排名") != std::wstring::npos) {
                    // 检测到表头，跳过这一行
                    isFirstLine = false;
                    continue;
                }
            }

            // 不是表头，标记第一行已处理
            isFirstLine = false;
        }

        // ----------------------------------------------------
        // 解析CSV行
        // ----------------------------------------------------
        std::vector<std::wstring> columns = SplitCsvLine(line);

        // 检查是否有足够的字段
        // 成绩清单需要至少3列：名次、组别、成绩时间
        if (columns.size() < 3) {
            continue;  // 字段不足，跳过这一行
        }

        // ----------------------------------------------------
        // 填充ScoreEntry结构体
        // ----------------------------------------------------
        // 列映射：
        // columns[0] = 名次（需要转换为整数）
        // columns[1] = 组别
        // columns[2] = 成绩时间
        // ----------------------------------------------------
        ScoreEntry entry;

        // ----------------------------------------------------
        // 转换名次为整数
        // ----------------------------------------------------
        // 使用 try-catch 捕获转换异常
        // 
        // std::stoi 可能抛出的异常：
        // - std::invalid_argument：无法转换为整数
        // - std::out_of_range：值超出int范围
        // 
        // 如果转换失败，设置 rank = 0
        // 后续会过滤掉 rank <= 0 的行
        // ----------------------------------------------------
        try {
            // std::stoi 将 wstring 转换为 int
            entry.rank = std::stoi(columns[0]);
        }
        catch (...) {
            // 转换失败，设置为0
            entry.rank = 0;
        }

        // 组别和成绩时间（字符串，不需要转换）
        entry.group = columns[1];
        entry.time = columns[2];

        // 从组别中提取组号
        // 用于后续匹配姓名
        entry.groupNumber = ExtractGroupNumber(entry.group);

        // ----------------------------------------------------
        // 添加到结果列表
        // ----------------------------------------------------
        // 过滤条件：名次 > 0
        // 这是为了跳过无效行（如表头、空行、格式错误的行）
        // ----------------------------------------------------
        if (entry.rank > 0) {
            scoreEntries.push_back(entry);
        }
    }

    return true;
}
