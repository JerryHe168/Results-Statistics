#pragma once
#include "DataTypes.h"
#include <vector>
#include <string>

// ============================================================
// ExcelReader 类 - Excel文件读取器
// ============================================================
// 负责从Excel文件（.xls, .xlsx格式）中读取数据
// 使用COM自动化技术与Excel交互
// 
// 主要功能：
// 1. 读取报名信息 - 包含男生编号、男生姓名、女生编号、女生姓名
// 2. 读取成绩清单 - 包含名次、组别、成绩
// ============================================================
class ExcelReader {
public:
    // --------------------------------------------------------
    // 构造函数
    // 初始化COM库，为Excel自动化做准备
    // --------------------------------------------------------
    ExcelReader();

    // --------------------------------------------------------
    // 析构函数
    // 释放COM库资源
    // --------------------------------------------------------
    ~ExcelReader();

    // --------------------------------------------------------
    // 读取报名信息
    // --------------------------------------------------------
    // 参数：
    //   filePath - Excel文件路径
    //   participants - 输出参数，存储读取到的报名信息列表
    // 
    // 返回值：
    //   true  - 读取成功
    //   false - 读取失败
    // 
    // Excel文件格式要求：
    //   第1列：男生编号（如 "12A", "18A"）
    //   第2列：男生姓名
    //   第3列：女生编号（如 "17B", "13B"）
    //   第4列：女生姓名
    //   第1行：表头（可选，会自动跳过）
    // --------------------------------------------------------
    bool ReadRegistrationInfo(const std::wstring& filePath, std::vector<Participant>& participants);

    // --------------------------------------------------------
    // 读取成绩清单
    // --------------------------------------------------------
    // 参数：
    //   filePath - Excel文件路径
    //   scoreEntries - 输出参数，存储读取到的成绩条目列表
    // 
    // 返回值：
    //   true  - 读取成功
    //   false - 读取失败
    // 
    // Excel文件格式要求：
    //   第1列：名次（如 1, 2, 3）
    //   第2列：组别（如 "13组", "22组"）
    //   第3列：成绩时间（如 "0:37:06"）
    //   第1行：表头（可选，会自动跳过）
    // --------------------------------------------------------
    bool ReadScoreList(const std::wstring& filePath, std::vector<ScoreEntry>& scoreEntries);

private:
    // --------------------------------------------------------
    // 从编号中提取组号
    // --------------------------------------------------------
    // 参数：
    //   id - 编号字符串，如 "12A", "18B", "13组"
    // 
    // 返回值：
    //   提取的组号（整数），如 12, 18, 13
    //   如果无法提取数字，返回 -1
    // 
    // 工作原理：
    //   使用正则表达式匹配字符串中的第一个连续数字序列
    //   支持的格式：
    //   - "12A"   -> 12
    //   - "18B"   -> 18
    //   - "13组"  -> 13
    //   - "第5组" -> 5
    // --------------------------------------------------------
    int ExtractGroupNumber(const std::wstring& id);
};
